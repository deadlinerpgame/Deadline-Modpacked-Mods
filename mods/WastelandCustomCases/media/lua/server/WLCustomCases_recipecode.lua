function WLCustomCases.CarPartsPileAcceptFunction(_, item)
  local category = item:getDisplayCategory()
  local itemId = item:getFullType()
  if itemId == "Base.WPI_Package" then
    local fakeItem = InventoryItemFactory.CreateItem(item:getModData()['WPIPackages'].contents.itemName)
    category = fakeItem:getDisplayCategory()
  end
  return category == "Mech" or category == "VehicleMaintenance" or WLCustomCases.CarPartsPileAllowedTypes[itemId]
end

