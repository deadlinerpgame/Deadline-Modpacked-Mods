for _, itemName in ipairs(WLCustomCases.MagPouchItems) do
  local item = getScriptManager():getItem(itemName)
  if item then
    WL_Utils.setItemProperties(itemName, {
      ["AcceptItemFunction"] = "WLCustomCases.MagPouchAcceptFunction",
      ["WeightReduction"] = "90",
      ["Capacity"] = "6",
    })
  end
end

function WLCustomCases.MagPouchAcceptFunction(_, item)
  local category = item:getDisplayCategory()
  if item:getFullType() == "Base.WPI_Package" then
    local fakeItem = InventoryItemFactory.CreateItem(item:getModData()['WPIPackages'].contents.itemName)
    category = fakeItem:getDisplayCategory()
  end
  if item:IsInventoryContainer() then
    return false
  end
  return WLCustomCases.MagPouchAllowedCategory[category]
end

for _, itemName in ipairs(WLCustomCases.SecondaryStorageItems) do
  local item = getScriptManager():getItem(itemName)
  if item then
    WL_Utils.setItemProperties(itemName, {
      ["AcceptItemFunction"] = "WLCustomCases.SecondaryStorageAcceptFunction",
    })
  end
end

function WLCustomCases.SecondaryStorageAcceptFunction(_, item)
  if item:IsInventoryContainer() then
    return false
  end
  return true
end

function WLCustomCases.CanDestroyPile(item)
  return item:getItemContainer():isEmpty()
end

function WLCustomCases.CanBuildPile(item)
  return item:getContainer():getType() == "floor"
end
