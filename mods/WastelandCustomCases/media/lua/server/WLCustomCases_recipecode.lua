function WLCustomCases.WalletAcceptFunction(_, item)
  local itemId = item:getFullType()
  if itemId == "Base.WPI_Package" then
    itemId = item:getModData()['WPIPackages'].contents.itemName
  end
  if getActivatedMods():contains("TrueActionsDancing") then
    if string.find(itemId, "TAD.BobTA_") and not string.find(itemId, "_Mag") then
      return true
    end
  end
  return WLCustomCases.WalletAllowedTypes[itemId]
end

